#Operadores de associação são usados para verificar se um valor está presente em uma sequência

fruta = ["laranja", "uva", "pera", "abacaxi"]

check = "laranja" in fruta
print(check)
#in, é pra ver se um valor esta na variavel


check_02 = "laranja" not in fruta
print(check_02)
#not in, retorna true se o valor não estiver na sequencia